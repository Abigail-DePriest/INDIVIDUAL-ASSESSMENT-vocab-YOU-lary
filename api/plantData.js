import client from '../utils/client';

const endpoint = client.databaseURL;

const getPlants = () => new Promise((resolve, reject) => {
  fetch(`${endpoint}/plants.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(Object.values(data)))
    .catch(reject);
});

const deletePlant = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/plants/${firebaseKey}.json`, {
    method: 'DELETE',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const getSinglePlant = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/plants/${firebaseKey}.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    }, // you technically do not need the options object for GET requests, but using it here for consistency
  })
    .then((response) => response.json())
    .then((data) => resolve(data)) // will resolve a single object
    .catch(reject);
});

const createPlant = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/plants.json`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const updatePlants = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/plants/${payload.firebaseKey}.json`, {
    method: 'PATCH',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then(resolve)
    .catch(reject);
});

export {
  getPlants,
  deletePlant,
  createPlant,
  updatePlants,
  getSinglePlant
};
